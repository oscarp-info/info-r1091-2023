#include <stdio.h>

int main(){

    int i;

    printf("\nEjemplo de for:\n");
    for ( i = 0; i < 10; i++){
        printf("i = %d\n", i);
    }


    printf("\nEjemplo de simular for con while:\n");
    i = 0;
    while (  i < 10 ){
        printf("i = %d\n", i);
        i++;
    }


    return 0;
}



